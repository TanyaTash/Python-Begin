# Задача 12: Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница.
# Петя помогает Кате по математике. Он задумывает два натуральных числа X и Y (X,Y≤1000), а Катя должна их отгадать.
# Для этого Петя делает две подсказки. Он называет сумму этих чисел S и их произведение P. Помогите Кате отгадать задуманные Петей числа.
#
# Пример
# Введите сумму (S): 10
# Введите произведение (P): 24
# Задуманные числа: X = 4, Y = 6

# Функция для решения задачи
def solveNumbers(S, P):
    # Перебор всех возможных значений X и Y
    for X in range(1, 1001):
        for Y in range(1, 1001):
            # Проверка условий задачи
            if X + Y == S and X * Y == P:
                return X, Y  # Возвращаем найденные значения X и Y

    return None  # Если решение не найдено, возвращаем None

# Ввод значений суммы и произведения
S = int(input("Enter the amount (S): "))  # Ввод суммы S
P = int(input("Enter the product of numbers (P): "))  # Ввод произведения P

# Решение задачи
result = solveNumbers(S, P)  # Вызов функции для решения задачи

if result:
    X, Y = result
    print(f"Perceived numbers: X = {X}, Y = {Y}")  # Вывод результата, если решение найдено
else:
    print("No solution has been found.")  # Вывод сообщения, если решение не найдено
